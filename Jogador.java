public class Jogador {

    public String nome;
    public float peso;
    public int idade;
    public int qtdCartoes;
    public int qtdGolsTemp;
    public double salario;

    @Override
    public String toString() {
        return "Jogador{" +
                "nome='" + nome + '\'' +
                ", peso=" + peso +
                ", idade=" + idade +
                ", qtdCartoes=" + qtdCartoes +
                ", qtdGolsTemp=" + qtdGolsTemp +
                ", salario=" + salario +
                '}';
    }
}

